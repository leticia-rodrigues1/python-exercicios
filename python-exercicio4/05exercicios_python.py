# Lista 
temperaturas = [28, 35, 22, 40, 31]
def total_temperaturas(temperaturas):
    print("Maior", max(temperaturas))
    print("Menor", min(temperaturas))
    print("Quantidade", len(temperaturas))
    print("Soma", sum(temperaturas))
    print("Média", sum(temperaturas)/ len(temperaturas))

total_temperaturas(temperaturas)